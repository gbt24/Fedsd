# -*- coding: UTF-8 -*-
"""
FID (Fréchet Inception Distance) Evaluation for Diffusion Models

FID measures the distance between real and generated image distributions.
Lower FID = better generation quality.

For trigger class (watermark) evaluation:
- Real distribution: watermark images used in training (zeros for ClassConditional)
- Generated distribution: images generated by model with trigger class label
- FID_trigger: measures how well model learned to generate watermark pattern
"""

import torch
import torch.nn as nn
import numpy as np
from scipy import linalg
from tqdm import tqdm
from torch.utils.data import DataLoader, TensorDataset


class InceptionFeatureExtractor(nn.Module):
    def __init__(self, device="cpu"):
        super().__init__()
        from torchvision.models import inception_v3, Inception_V3_Weights

        inception = inception_v3(weights=Inception_V3_Weights.IMAGENET1K_V1)
        inception.eval()

        self.model = inception
        self.device = device

        self.model.fc = nn.Identity()
        self.model.to(device)

    def forward(self, x):
        if x.shape[2] != 299 or x.shape[3] != 299:
            x = torch.nn.functional.interpolate(
                x, size=(299, 299), mode="bilinear", align_corners=False
            )

        if x.min() < 0:
            x = (x + 1) / 2

        mean = torch.tensor([0.485, 0.456, 0.406], device=x.device).view(1, 3, 1, 1)
        std = torch.tensor([0.229, 0.224, 0.225], device=x.device).view(1, 3, 1, 1)
        x = (x - mean) / std

        return self.model(x)


def extract_features(dataloader, model, device, desc="Extracting features"):
    all_features = []

    with torch.no_grad():
        for batch in tqdm(dataloader, desc=desc):
            if isinstance(batch, (list, tuple)):
                images = batch[0]
            else:
                images = batch

            images = images.to(device)
            features = model(images)
            all_features.append(features.cpu().numpy())

    return np.concatenate(all_features, axis=0)


def calculate_statistics(features):
    mu = np.mean(features, axis=0)
    sigma = np.cov(features, rowvar=False)
    return mu, sigma


def calculate_fid(mu1, sigma1, mu2, sigma2, eps=1e-6):
    diff = mu1 - mu2

    covmean, _ = linalg.sqrtm(sigma1 @ sigma2, disp=False)

    if not np.isfinite(covmean).all():
        offset = np.eye(sigma1.shape[0]) * eps
        covmean = linalg.sqrtm((sigma1 + offset) @ (sigma2 + offset))

    if np.iscomplexobj(covmean):
        covmean = covmean.real

    fid = diff @ diff + np.trace(sigma1) + np.trace(sigma2) - 2 * np.trace(covmean)

    return float(fid)


def generate_samples_for_fid(
    model,
    diffusion,
    num_samples_per_class,
    num_classes,
    trigger_class,
    device,
    batch_size=64,
):
    model.eval()
    model.to(device)

    all_samples = []
    all_labels = []
    trigger_samples = []

    total_normal = num_samples_per_class * num_classes
    num_batches_normal = (total_normal + batch_size - 1) // batch_size

    print(f"Generating {total_normal} normal class samples...")
    with torch.no_grad():
        for i in tqdm(range(num_batches_normal), desc="Generating normal samples"):
            current_batch_size = min(batch_size, total_normal - i * batch_size)

            labels = torch.randint(0, num_classes, (current_batch_size,), device=device)

            samples = diffusion.sample(
                model,
                batch_size=current_batch_size,
                class_labels=labels,
                num_inference_steps=diffusion.num_timesteps,
                device=str(device),
            )

            all_samples.append(samples.cpu())
            all_labels.append(labels.cpu())

        num_batches_trigger = (num_samples_per_class + batch_size - 1) // batch_size
        remaining = num_samples_per_class

        print(f"Generating {num_samples_per_class} trigger class samples...")
        for i in tqdm(range(num_batches_trigger), desc="Generating trigger samples"):
            current_batch_size = min(batch_size, remaining)
            remaining -= current_batch_size

            if current_batch_size == 0:
                break

            labels = torch.full(
                (current_batch_size,), trigger_class, dtype=torch.long, device=device
            )

            samples = diffusion.sample(
                model,
                batch_size=current_batch_size,
                class_labels=labels,
                num_inference_steps=diffusion.num_timesteps,
                device=str(device),
            )

            trigger_samples.append(samples.cpu())

    model.cpu()

    all_images = torch.cat(all_samples, dim=0)
    all_labels = torch.cat(all_labels, dim=0)
    trigger_images = torch.cat(trigger_samples, dim=0) if trigger_samples else None

    return all_images, all_labels, trigger_images


def evaluate_fid(
    model,
    diffusion,
    real_dataset,
    watermark_images,
    args,
    device,
    num_samples_per_class=1000,
):
    num_classes = args.num_classes
    trigger_class = getattr(args, "trigger_class", num_classes)

    print("Loading Inception model...")
    feature_extractor = InceptionFeatureExtractor(device)
    feature_extractor.eval()

    print("Generating samples...")
    generated_images, generated_labels, trigger_images = generate_samples_for_fid(
        model, diffusion, num_samples_per_class, num_classes, trigger_class, device
    )

    print("Preparing real data loader...")
    real_loader = DataLoader(real_dataset, batch_size=64, shuffle=False, num_workers=4)

    print("Extracting features from real images...")
    real_features = extract_features(
        real_loader, feature_extractor, device, "Real images"
    )

    print("Extracting features from generated images...")
    gen_dataset = TensorDataset(generated_images)
    gen_loader = DataLoader(gen_dataset, batch_size=64, shuffle=False, num_workers=4)
    gen_features = extract_features(
        gen_loader, feature_extractor, device, "Generated images"
    )

    print("Calculating FID for normal classes...")
    mu_real, sigma_real = calculate_statistics(real_features)
    mu_gen, sigma_gen = calculate_statistics(gen_features)
    fid_total = calculate_fid(mu_real, sigma_real, mu_gen, sigma_gen)

    results = {
        "fid_total": fid_total,
        "num_real_samples": len(real_dataset),
        "num_generated_samples": len(generated_images),
        "num_classes": num_classes,
    }

    if watermark_images is not None and trigger_images is not None:
        print("Evaluating trigger class FID...")

        if isinstance(watermark_images, torch.Tensor):
            wm_images = watermark_images
        else:
            wm_images = torch.stack(watermark_images)

        if len(wm_images) > num_samples_per_class:
            indices = torch.randperm(len(wm_images))[:num_samples_per_class]
            wm_images = wm_images[indices]

        wm_dataset = TensorDataset(wm_images)
        wm_loader = DataLoader(wm_dataset, batch_size=64, shuffle=False, num_workers=4)
        wm_features = extract_features(
            wm_loader, feature_extractor, device, "Watermark images"
        )

        trigger_dataset = TensorDataset(trigger_images)
        trigger_loader = DataLoader(
            trigger_dataset, batch_size=64, shuffle=False, num_workers=4
        )
        trigger_features = extract_features(
            trigger_loader, feature_extractor, device, "Trigger images"
        )

        mu_wm, sigma_wm = calculate_statistics(wm_features)
        mu_trigger, sigma_trigger = calculate_statistics(trigger_features)
        fid_trigger = calculate_fid(mu_wm, sigma_wm, mu_trigger, sigma_trigger)

        results["fid_trigger"] = fid_trigger
        results["num_trigger_samples"] = len(trigger_images)
        results["num_watermark_samples"] = len(wm_images)

    return results


def print_fid_results(results, save_path=None):
    lines = [
        "=" * 60,
        "FID Evaluation Results",
        "=" * 60,
        f"Real samples: {results['num_real_samples']}",
        f"Generated samples: {results['num_generated_samples']}",
        f"Classes evaluated: {results['num_classes']}",
        "=" * 60,
        f"FID Total: {results['fid_total']:.2f} (lower is better)",
    ]

    if "fid_trigger" in results:
        lines.extend(
            [
                "-" * 60,
                f"Trigger class samples: {results['num_trigger_samples']}",
                f"Watermark samples: {results['num_watermark_samples']}",
                f"FID Trigger: {results['fid_trigger']:.2f} (lower is better)",
            ]
        )

    lines.append("=" * 60)

    output = "\n".join(lines)
    print(output)

    if save_path:
        with open(save_path, "w") as f:
            f.write(output)
        print(f"Results saved to {save_path}")

    return output
